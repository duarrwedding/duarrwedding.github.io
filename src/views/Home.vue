<script>
import CountDown from '@/components/CountDown.vue'
import { createUser, useLoadUsers } from '@/firebase'
import SpinnerDot from '@/components/SpinnerDot.vue'
import { reactive } from 'vue'

export default {
  name: 'WeddingContent',
  components: {
    CountDown,
    SpinnerDot
  },
  data() {
    return {
      button: false,
      formMessage: ''
    }
  },
  mounted() {
    // document.getElementsByTagName('audio')[0].play()
    const observer = new IntersectionObserver(entries => {
      // We will fill in the callback later...
      entries.forEach(entry => {
        // If the element is visible
        if (entry.isIntersecting) {
          // Add the animation class
          entry.target.classList.add('animate')
        }
      })
    })
    // Tell the observer which elements to track
    observer.observe(document.querySelector('.rr-table-3'))
    observer.observe(document.querySelector('.mempelai-4'))
    observer.observe(document.querySelector('.name-pr'))
    observer.observe(document.querySelector('.dengan'))
    observer.observe(document.querySelector('.name-lk'))
    observer.observe(document.querySelector('.btn-google-maps-6'))
    observer.observe(document.querySelector('.photo-description-6'))
  },
  setup() {
    const form = reactive({
      name: '',
      relation: '',
      attendance: '',
      message: ''
    })
    // let users = ''
    const users = useLoadUsers()
    const onSubmit = async () => {
      try {
        document.getElementById('form-text-id').style.display = 'none'
        document.getElementsByClassName('button-kirim')[0].style.display =
          'none'
        document.getElementsByClassName('spinner-home')[0].style.display =
          'block'

        const { name, relation, attendance, message } = form
        if (name && relation && attendance && message) {
          await createUser({ ...form })
          form.name = ''
          form.relation = ''
          form.attendance = ''
          form.message = ''
          document.getElementById('form-text-id').style.color = 'green'
        } else {
          throw Error
        }
      } catch (err) {
        document.getElementById('form-text-id').innerHTML =
          'Mohon lengkapi form'
        document.getElementById('form-text-id').style.color = 'red'
        document.getElementById('form-text-id').style.display = 'inline'
      } finally {
        document.getElementsByClassName('button-kirim')[0].style.display =
          'block'
        document.getElementsByClassName('spinner-home')[0].style.display =
          'none'
        console.log('--->', document.getElementById('form-text-id').style.color)
        if (document.getElementById('form-text-id').style.color !== 'red') {
          document.getElementById('form-text-id').innerHTML =
            'Berhasil kirim form'
          document.getElementById('form-text-id').style.display = 'inline'
          setTimeout(async () => {
            document.getElementById('form-text-id').style.display = 'none'
          }, 3000)
        }
      }
    }
    return { users, form, onSubmit }
  }
}
</script>

